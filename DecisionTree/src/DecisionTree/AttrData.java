package DecisionTree;

/**
 * Store a pair of attribute name and attribute value in a data set.
 * @author FenglanYang
 *
 */
public class AttrData {
    String name;    // attribute name
    int value;  // attribute value
    
    public AttrData(String name, int value) {
        this.name = name;
        this.value = value;
    }
}
